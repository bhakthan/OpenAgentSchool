/** Returns every series across all categories. */
export function getAllSeries(): ListeningSeries[] {
  return ALL_SERIES;
}

/** Returns series filtered by category. */
export function getSeriesByCategory(category: SeriesCategory): ListeningSeries[] {
  return ALL_SERIES.filter((s) => s.category === category);
}

/** Returns a single series by ID, or undefined. */
export function getSeries(seriesId: string): ListeningSeries | undefined {
  return ALL_SERIES.find((s) => s.id === seriesId);
}

/** Returns a single episode by ID, searching across all series. */
export function getEpisode(episodeId: string): ListeningEpisode | undefined {
  for (const series of ALL_SERIES) {
    const ep = series.episodes.find((e) => e.id === episodeId);
    if (ep) return ep;
  }
  return undefined;
}

/** Returns every episode across all series, flattened. */
export function getAllEpisodes(): ListeningEpisode[] {
  return ALL_SERIES.flatMap((s) => s.episodes);
}

/** Returns episodes that link to a specific concept ID. */
export function getEpisodesByConceptId(conceptId: string): ListeningEpisode[] {
  return getAllEpisodes().filter((e) => e.conceptId === conceptId);
}
